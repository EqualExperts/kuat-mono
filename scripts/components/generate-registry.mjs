#!/usr/bin/env node
// Generate the manifest-derived component-registry rows.
//
// Emits `kuat-docs/components/registry.generated.md` — the ID / Display name /
// Source / doc-path rows derived from `components.manifest.json`. This is the
// canonical row set a contributor pastes into the upstream
// `reference/media-types/web-product/component-registry.md` (kuat-agent-rules),
// reconciling the curated "Source" prose and "Upstream refs" links by hand.
//
// The upstream registry stays the human-owned file; this generator + the
// `check-registry.mjs` drift gate are what keep it from going stale.
//
// Usage: node scripts/components/generate-registry.mjs

import { writeFile } from "node:fs/promises";
import path from "node:path";
import {
  GENERATED_REGISTRY_PATH,
  componentList,
  formatSource,
  readManifest,
  repoRoot,
} from "./lib.mjs";

const manifest = await readManifest();
const components = componentList(manifest);

const missingDisplayName = components.filter((c) => !c.displayName);
if (missingDisplayName.length) {
  console.error(
    `generate-registry: missing "displayName" for: ${missingDisplayName
      .map((c) => c.id)
      .join(", ")}`,
  );
  process.exit(1);
}

const header = [
  "<!-- GENERATED by scripts/components/generate-registry.mjs — do not hand-edit. -->",
  "",
  "# Component registry rows (generated)",
  "",
  "Derived from `kuat-docs/components/components.manifest.json`. Paste these rows into the",
  "upstream `reference/media-types/web-product/component-registry.md` (kuat-agent-rules),",
  'reconciling the curated "Source" prose and "Upstream refs" links by hand. The',
  "`pnpm components:registry:check` drift gate enforces ID / display-name / doc-path parity.",
  "",
  "| ID | Display name | Source | Overlay / package doc | Upstream refs |",
  "|----|--------------|--------|----------------------|---------------|",
];

const rows = components.map(
  (c) =>
    `| \`${c.id}\` | ${c.displayName} | ${formatSource(c.sources)} | \`${c.path}\` | — |`,
);

const output = `${[...header, ...rows].join("\n")}\n`;
await writeFile(GENERATED_REGISTRY_PATH, output, "utf8");

console.log(
  `Generated ${components.length} registry rows → ${path.relative(repoRoot, GENERATED_REGISTRY_PATH)}`,
);
