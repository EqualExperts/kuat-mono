#!/usr/bin/env node
/**
 * Reads component CSS in deterministic order (sync, one file at a time) and
 * writes a single bundle so Tailwind does not hit EMFILE from wide Promise.all
 * @import fan-out during `vite build`.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const pkgRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const bundlesDir = join(pkgRoot, "src/components-bundles");
const bundles = ["bundle-a.css", "bundle-b.css", "bundle-c.css"];

let combined =
  "/* auto-generated by scripts/concat-component-styles.mjs — do not edit */\n";

for (const name of bundles) {
  const bundlePath = join(bundlesDir, name);
  const bundleSrc = readFileSync(bundlePath, "utf8");
  for (const line of bundleSrc.split(/\r?\n/)) {
    const m = line.match(/^\s*@import\s+["']([^"']+)["']\s*;?\s*$/);
    if (!m) continue;
    const target = resolve(bundlesDir, m[1]);
    combined += `\n/* ---- ${name} -> ${m[1]} ---- */\n`;
    let body = readFileSync(target, "utf8");
    // Drop @reference to styles.css: _inlined is imported FROM styles.css — a back-reference is circular
    // and breaks Tailwind resolve ("Can't resolve …/styles.css" from components-bundles/).
    body = body.replace(/^@reference\s+["'][^"']*styles\.css["']\s*;?\s*$/gm, "");
    combined += body;
    combined += "\n";
  }
}

const outFile = join(bundlesDir, "_inlined-components.css");
writeFileSync(outFile, combined);
console.log("[concat-component-styles] wrote", outFile, `(${combined.length} chars)`);
